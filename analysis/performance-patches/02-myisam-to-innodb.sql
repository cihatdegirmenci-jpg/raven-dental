-- Performance Patch 02 — MyISAM → InnoDB
-- Hedef: 161 MyISAM tabloyu InnoDB'ye dönüştür
-- Avantajlar:
--   - Row-level locking (concurrent yazma)
--   - Crash recovery
--   - Buffer pool cache (better hit rate)
--   - Foreign keys
-- Risk: Düşük (FULLTEXT yok, küçük DB ~4 MB)
-- Süre: ~1-3 saniye toplam (local), 10-30 sn (production)
-- Beklenen kazanım: Concurrent yazmada 2-5x, okumada %10-20

